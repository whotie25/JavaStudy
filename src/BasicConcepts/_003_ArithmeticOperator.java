package BasicConcepts;

public class _003_ArithmeticOperator {
	public static void main(String[] args) {
		float sqrt_2 = 1.414f;
		int num = 4;
		
		System.out.println(sqrt_2 + " + " + num + " = " + (sqrt_2+num));
		System.out.println(sqrt_2 + " * " + num + " = " + (sqrt_2*num) + "\n");
		
		System.out.println("3 / " + num + " = " + (3/num));
		System.out.println("3.0 / " + num + " = " + (3.0/num));
	}
}